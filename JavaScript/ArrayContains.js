const contains = (initialObjectArray, comparativeObjectArray) => {
  const excludedItems = comparativeObjectArray.filter((comparative) =>
    initialObjectArray.every(
      (obj) =>
        obj.label !== comparative.label ||
        obj.name !== comparative.name ||
        JSON.stringify(obj.options) !== JSON.stringify(comparative.options)
    )
  );
  return excludedItems.length < 0;
}; //

